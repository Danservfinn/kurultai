def extract_frontmatter(task_path: Path) -> dict:
    """Alias for extract_yaml_frontmatter that accepts Path."""
    try:
        with open(task_path, 'r') as f:
            content = f.read(5000)
        return extract_yaml_frontmatter(content)
    except Exception:
        return {}


def save_audit_result(result: AuditResult, task_id: str = None) -> str:
    """Alias for save_audit_log with compatible signature."""
    task_path = Path(AGENTS_DIR) / "main" / "tasks" / f"{task_id}.md"
    return save_audit_log(result, task_path)


# CLI interface
if __name__ == "__main__":
    import argparse

    parser = argparse.ArgumentParser(description="Completion Gate Audit")
    parser.add_argument("task_file", help="Path to task file (.executing.md)")
    parser.add_argument("--agent", default="temujin", help="Agent who executed the task")
    parser.add_argument("--json", action="store_true", help="Output as JSON")
    parser.add_argument("--no-gate-check", action="store_true", help="Skip should_run_gate check")

    args = parser.parse_args()

    if not args.no_gate_check and not should_run_gate(args.task_file):
        result = {"skipped": True, "reason": "Task opted out of completion gate"}
        print(json.dumps(result) if args.json else "Gate skipped: task opted out")
        exit(0)

    result = completion_gate_audit(args.task_file, args.agent)

    if args.json:
        print(json.dumps(result.to_dict(), indent=2))
    else:
        print(f"Completion Gate Audit: {result.original_task}")
        print(f"  Completion: {result.completion_percentage}%")
        print(f"  Can Complete: {result.can_complete}")
        if result.missing_components:
            print(f"  Missing: {', '.join(result.missing_components)}")
        if result.required_followups:
            print(f"  Follow-ups Required: {len(result.required_followups)}")
            for f in result.required_followups:
                print(f"    - {f['title']} ({f['agent']}, {f['priority']})")
        if result.blockers:
            print(f"  Blockers: {', '.join(result.blockers)}")

    exit(0 if result.can_complete else 1)
